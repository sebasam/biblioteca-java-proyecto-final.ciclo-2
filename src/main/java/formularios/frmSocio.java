    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            if(txtIdSocio.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Consulta el socio a eliminar", "Error al eliminar al socio", JOptionPane.ERROR_MESSAGE);
            }else{
                int intId = Integer.parseInt(txtIdSocio.getText());
                Socio socio = new Socio(intId);
                SocioDao sociodao = new SocioDao();
                sociodao.eliminar_socio(socio);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error al eliminar al socio " + e, "Algo ha ido mal!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

