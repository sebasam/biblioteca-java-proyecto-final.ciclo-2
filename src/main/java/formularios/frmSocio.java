    private void btnRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroActionPerformed
        // TODO add your handling code here:
        String strNombre = txtNombre.getText();
        String strTelefono = txtTelefono.getText();
        String strDireccion = txtDireccion.getText();
        String strEmail = txtEmail.getText();
        String strMulta = "No";

        try{
            if(strNombre.length() == 0 || strTelefono.length() == 0 || strDireccion.length() == 0 || strEmail.length() == 0){
                JOptionPane.showMessageDialog(this, "Los campos no pueden estar vacíos!", "Error en registro!", JOptionPane.ERROR_MESSAGE);
            }else{
                Socio socio = new Socio(strNombre, strTelefono, strDireccion, strEmail, strMulta);
                boolean bolSocioRegistrado = false;
                System.out.println(strNombre.length());

                SocioDao sociodao = new SocioDao();
                bolSocioRegistrado = sociodao.registrar_socio(socio);

                if(!bolSocioRegistrado){
                    JOptionPane.showMessageDialog(this, "Socio Registrado correctamente", "Todo ha ido bien!", JOptionPane.INFORMATION_MESSAGE);
                    txtNombre.setText("");
                    txtTelefono.setText("");
                    txtDireccion.setText("");
                    txtEmail.setText("");
                }else{
                    JOptionPane.showMessageDialog(this, "Error al registrar al nuevo socio", "Error en registro!", JOptionPane.ERROR_MESSAGE);
                }
            }

        }catch(Exception error){
            JOptionPane.showMessageDialog(this, "Error al ingresar al nuevo socio" + error, "Error en registro!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistroActionPerformed

