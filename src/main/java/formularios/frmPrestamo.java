    private void jtbPrestamoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbPrestamoMouseClicked
        // TODO add your handling code here:
        int index = jtbPrestamo.getSelectedRow();
        Object id = jtbPrestamo.getValueAt(index, 0);
        String strId = "" + id;
        txtId.setText(strId);
        
        int intId = Integer.parseInt(strId);
        Prestamo prestamo = new Prestamo(intId);
        PrestamoDao prestamodao = new PrestamoDao();
        Prestamo myPrestamo = prestamodao.consultar_socio(prestamo);
        txtNombre.setText(Integer.toString(myPrestamo.getId_prestamo()));
        
    }//GEN-LAST:event_jtbPrestamoMouseClicked

    private void btnEntregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntregarActionPerformed
        // TODO add your handling code here:
        
        String strFecha = txtFecha.getText();
        int index = jtbPrestamo.getSelectedRow();
        Object objFechaEstimada = jtbPrestamo.getValueAt(index, 2);
        String strFechaEstimada = "" + objFechaEstimada;
        
        PrestamoDao prestamodao = new PrestamoDao();
        SocioDao sociodao = new SocioDao();
        DetallePrestamoDao detalledao = new DetallePrestamoDao();
        
        try {
            Date dtFechaEstimada = new SimpleDateFormat("yyyy-MM-dd").parse(strFechaEstimada);
            Date dtFecha = new SimpleDateFormat("yyyy-MM-dd").parse(strFecha);
            if(dtFecha.after(dtFechaEstimada)){
                String strObj = txtNombre.getText();
                int intIdSocio = Integer.parseInt(strObj);
                Socio socio = new Socio(intIdSocio);
                sociodao.generar_multa(socio);
                JOptionPane.showMessageDialog(this, "Te pasaste de la fecha de entrega, se ha generado una multa", "Ups!! algo ha ido mal", JOptionPane.WARNING_MESSAGE);
                
            }            
            Object objIdPrestamo = jtbPrestamo.getValueAt(index, 0);
            String strIdPrestamo = objIdPrestamo.toString();
            int intId = Integer.parseInt(strIdPrestamo);
            DetallePrestamo prestamo = new DetallePrestamo(intId);
            detalledao.editar_prestamo(prestamo);
            
            Object objIdLibro = jtbPrestamo.getValueAt(index, 1);
            String strIdLibro = "" + objIdLibro;
            int intIdLibro = Integer.parseInt(strIdLibro);
            
            Libro libro = new Libro(intIdLibro);
            sociodao.devolver_libro(libro);            
            
            JOptionPane.showMessageDialog(this, "El libro ha sido devuelto y el prestamo ha sido eliminado con éxito!!", "Excelente", JOptionPane.INFORMATION_MESSAGE);
            limpiarTabla();
            actualizarTabla();
                
            
        } catch (Exception error) {
            JOptionPane.showMessageDialog(this, "Error al entregar el libro!", "Ups!! algo ha ido mal", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnEntregarActionPerformed

