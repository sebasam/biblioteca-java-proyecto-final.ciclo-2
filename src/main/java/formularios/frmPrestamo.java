    public void actualizarTabla(){
        DefaultTableModel modeloTabla;
        modeloTabla = (DefaultTableModel)jtbPrestamo.getModel();
        Object[] obj_prestamo = new Object[4];
        
        PrestamoDao prestamodao = new PrestamoDao();
        
        ArrayList<DetallePrestamo> arr_prestamos = new ArrayList<DetallePrestamo>();
        arr_prestamos = prestamodao.actualizar_prestamo();
        
        //recorrer un array list
        Iterator<DetallePrestamo> iterator_prestamos = arr_prestamos.iterator();
        while(iterator_prestamos.hasNext()){
            DetallePrestamo objeto_prestamo = iterator_prestamos.next();
            obj_prestamo[0] = objeto_prestamo.getId_prestamo();
            obj_prestamo[1] = objeto_prestamo.getId_libro();
            obj_prestamo[2] = objeto_prestamo.getFecha_devolucion();
            obj_prestamo[3] = objeto_prestamo.getEstado();
            
            modeloTabla.addRow(obj_prestamo);
        }
        
        jtbPrestamo.setModel(modeloTabla);
    }
    
    
    
    private void limpiarTabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel)jtbPrestamo.getModel();
        int total_registros = jtbPrestamo.getRowCount() - 1;
        for(int i = total_registros; i >= 0; i--){
            modeloTabla.removeRow(i);
        }
    }
    

