    private void actualizarTabla(){
        DefaultTableModel modeloTabla;
        modeloTabla = (DefaultTableModel)jtbLibros.getModel();
        Object[] obj_libro = new Object[5];
        
        LibroDao librodao = new LibroDao();
        
        ArrayList<Libro> arr_libros = new ArrayList<Libro>();
        arr_libros = librodao.actualizar_tabla();
        
        Iterator<Libro> iterator_libros = arr_libros.iterator();
        while(iterator_libros.hasNext()){
            Libro objeto_libro = iterator_libros.next();
            obj_libro[0] = objeto_libro.getId_libro();
            obj_libro[1] = objeto_libro.getNombre();
            obj_libro[2] = objeto_libro.getEditorial();
            obj_libro[3] = objeto_libro.getAño();
            obj_libro[4] = objeto_libro.getEstado();
            
            modeloTabla.addRow(obj_libro);
        }
        
        jtbLibros.setModel(modeloTabla);
    }
    
    private void limpiarTabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel)jtbLibros.getModel();
        int total_registros = jtbLibros.getRowCount() - 1;
        for(int i = total_registros; i >= 0; i--){
            modeloTabla.removeRow(i);
        }
    }
    
    private void generar_combobox_autor(){
        cmbAutor.removeAllItems();
        ArrayList<Autor> arr_autores = new ArrayList<Autor>();
        AutorDao autordao = new AutorDao();
        arr_autores = autordao.autores_libro();
        Iterator<Autor> iterator_autor = arr_autores.iterator();
        
        while(iterator_autor.hasNext()){
            Autor objeto_autor = iterator_autor.next();
            cmbAutor.addItem(objeto_autor);
        }
        
    }
    
    private void generar_combobox_estado(){
        cmbEstado.addItem("Disponible");
        cmbEstado.addItem("Prestado");
        cmbEstado.addItem("En reparacion");        
    }

