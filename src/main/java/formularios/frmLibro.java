    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        Libro libro = new Libro();
        try{
            String strId = txtIdLibro.getText();
            int intId = Integer.parseInt(strId);            
            libro = new Libro(intId);
            
        }catch(Exception error){
            JOptionPane.showMessageDialog(this, "Error al consultar el libro", "Error!!", JOptionPane.ERROR_MESSAGE);
        }
        
        LibroDao librodao = new LibroDao();
        libro = librodao.consultar_libro(libro);
        int index = jtbLibros.getSelectedRow();
        Object objEstado = jtbLibros.getValueAt(index, 4);
        String strEstado = "" + objEstado;
        txtNombre.setText(libro.getNombre());
        txtEditorial.setText(libro.getEditorial());
        txtAño.setText(Integer.toString(libro.getAño()));
        txtIdAutor.setText(Integer.toString(libro.getId_autor()));
        cmbAutor.setSelectedIndex(libro.getId_autor() - 1);
        if(strEstado.contains("Prestado")){
            cmbEstado.setSelectedIndex(1);
        }else{
            cmbEstado.setSelectedIndex(0);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

