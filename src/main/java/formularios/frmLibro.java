    private void jtbLibrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbLibrosMouseClicked
        // TODO add your handling code here:
        int intSelect = jtbLibros.getSelectedRow() + 1;
        int index = jtbLibros.getSelectedRow();
        Object objEstado = jtbLibros.getValueAt(index, 4);
        String strEstado = "" + objEstado;
        if(strEstado.contains("Prestado")){
            cmbEstado.setSelectedIndex(1);
        }else{
            cmbEstado.setSelectedIndex(0);
        }
        txtIdLibro.setText(Integer.toString(intSelect));
    }//GEN-LAST:event_jtbLibrosMouseClicked

