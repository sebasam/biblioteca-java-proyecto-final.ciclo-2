    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try{
            String strNombre = txtNombre.getText();
            String strEditorial = txtEditorial.getText();
            String strAño = txtAño.getText();
            int intAño = Integer.parseInt(strAño);
            int intEstado = cmbEstado.getSelectedIndex();
            String strEstado = cmbEstado.getItemAt(intEstado);
            
            try{
                if(strEstado.equals("Disponible")|| strEstado.equals("Prestado") || strEstado.equals("En reparacion")){
                    int intIdAutor = cmbAutor.getSelectedIndex() + 1;  
                                       
                    
                    Libro libro = new Libro(strNombre, strEditorial, intAño, strEstado, intIdAutor);
                    boolean bolDatosGuardados = false;
                    System.out.println(intIdAutor);

                    LibroDao librodao = new LibroDao();
                    bolDatosGuardados = librodao.guardar_libro(libro);
                    if(!bolDatosGuardados){
                        JOptionPane.showMessageDialog(this, "Libro guardado correctamente!!", "Excelente", JOptionPane.INFORMATION_MESSAGE);
                        limpiarTabla();
                        actualizarTabla();
                        txtNombre.setText("");
                        txtEditorial.setText("");
                        txtAño.setText("");
                        txtIdAutor.setText(Integer.toString(cmbAutor.getSelectedIndex() + 1));
                        cmbEstado.setSelectedIndex(0);
                    }else{
                        JOptionPane.showMessageDialog(this, "Error al guardar el libro", "Error!!", JOptionPane.ERROR_MESSAGE);
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "El valor ingresado no es válido, los valores son Disponible, Prestado o En reparacion", "Error!! Ingresaste un estado incorrecto", JOptionPane.ERROR_MESSAGE);
                } 
            }catch(Exception error){
                System.out.println("Aqui esta el error" + error);
            }          
        }catch(Exception error){
            JOptionPane.showMessageDialog(this, "Error al ingresar los datos del libro" + error, "Error!!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

